package com.sunnyestate.data;

import java.util.ArrayList;
import java.util.List;

public class CategoryFilter {
	private int id;
	private String name = "";
	private List<CategoryFilterItem> filter_item_list = new ArrayList<CategoryFilterItem>();

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public List<CategoryFilterItem> getFilter_item_list() {
		return filter_item_list;
	}

	public void setFilter_item_list(List<CategoryFilterItem> filter_item_list) {
		this.filter_item_list = filter_item_list;
	}

}
